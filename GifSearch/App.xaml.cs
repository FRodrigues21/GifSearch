namespace GifSearch
{

    sealed partial class App : Application
    {

        public static string version;
        public static int pivot_index = 0;
        public static Frame rootFrame;

        public static Boolean user_showed { get; set; }
        public static int user_logged { get; set; }

        public static ObservableCollection<Result> trending { get; set; }
        public static ObservableCollection<Result> search { get; set; }


        public App()
        {
            Microsoft.ApplicationInsights.WindowsAppInitializer.InitializeAsync(
                Microsoft.ApplicationInsights.WindowsCollectors.Metadata |
                Microsoft.ApplicationInsights.WindowsCollectors.Session);
            this.InitializeComponent();
            this.Suspending += OnSuspending;
            version = string.Format("{0}.{1}.{2}.{3}",
                    Package.Current.Id.Version.Major,
                    Package.Current.Id.Version.Minor,
                    Package.Current.Id.Version.Build,
                    Package.Current.Id.Version.Revision);
        }
        
        protected override void OnLaunched(LaunchActivatedEventArgs e)
        {

            rootFrame = Window.Current.Content as Frame;

            if (rootFrame == null)
            {
                rootFrame = new Frame();

                rootFrame.NavigationFailed += OnNavigationFailed;

                if (e.PreviousExecutionState == ApplicationExecutionState.Terminated)
                {
                    //TODO: Load state from previously suspended application
                }

                Window.Current.Content = rootFrame;
            }

            if (rootFrame.Content == null)
            {
                user_showed = false;
                user_logged = (UserFacade.getLogged() + 1);
                UserFacade.setLogged(user_logged);
                rootFrame.Navigate(typeof(Tabs), e.Arguments);
            }

            if (ApiInformation.IsApiContractPresent("Windows.Phone.PhoneContract", 1, 0))
            {
                Windows.Phone.UI.Input.HardwareButtons.BackPressed += (s, a) =>
                {
                    Debug.WriteLine("BackPressed");
                    if (rootFrame.CanGoBack)
                    {
                        rootFrame.GoBack();
                        a.Handled = true;
                    }
                };
            }

            Window.Current.Activate();
        }

        void OnNavigationFailed(object sender, NavigationFailedEventArgs e)
        {
            throw new Exception("Failed to load Page " + e.SourcePageType.FullName);
        }

        private void OnSuspending(object sender, SuspendingEventArgs e)
        {
            var deferral = e.SuspendingOperation.GetDeferral();
            deferral.Complete();
        }

    }
}
